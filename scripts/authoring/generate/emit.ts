import { moduleCodeOf } from './ids.ts'
import type { LessonMeta } from './frontmatter.ts'
import type { LevelDef } from '../../../src/content/structure.ts'

// Single-quoted string literal, matching the codebase's quote style.
const q = (s: string): string => `'${s.replace(/\\/g, '\\\\').replace(/'/g, "\\'")}'`

export function emitCurriculum(structure: LevelDef[], metas: LessonMeta[]): string {
  const byModule = new Map<string, LessonMeta[]>()
  for (const m of metas) {
    byModule.set(moduleCodeOf(m.dottedId), [...(byModule.get(moduleCodeOf(m.dottedId)) ?? []), m])
  }
  for (const arr of byModule.values()) arr.sort((a, b) => (a.order ?? 0) - (b.order ?? 0))

  const lessonPathById: Record<string, string> = {}
  const levels = [...structure].sort((a, b) => a.order - b.order)

  const levelSrc = levels.map((level) => {
    const modules = [...level.modules].sort((a, b) => a.order - b.order)
    const moduleSrc = modules.map((mod) => {
      const lessons = byModule.get(mod.code) ?? []
      const lessonSrc = lessons.map((m) => {
        lessonPathById[m.dottedId] = `/learn/${level.id}/${mod.slug}/${m.slug}`
        return emitLesson(m, `./lessons/${level.id}/${m.slug}.mdx`)
      })
      return `    {\n      id: ${q(mod.slug)},\n      title: ${q(mod.title)},\n      lessons: [\n${lessonSrc.join(',\n')},\n      ],\n    }`
    })
    return `  {\n    id: ${q(level.id)},\n    title: ${q(level.title)},\n    modules: [\n${moduleSrc.join(',\n')},\n    ],\n  }`
  })

  const mapSrc = Object.entries(lessonPathById)
    .map(([k, v]) => `  ${q(k)}: ${q(v)},`)
    .join('\n')

  return (
    `// DO NOT EDIT — generated by scripts/authoring/generateCurriculum.ts\n` +
    `// Source of truth: src/content/structure.ts + lesson frontmatter in src/content/lessons/**\n\n` +
    `import type { Level } from './curriculum.types'\n\n` +
    `export type { Lesson, Module, Level, LessonType, Volatility, InteractiveKind, InteractiveRef } from './curriculum.types'\n\n` +
    `export const curriculum: Level[] = [\n${levelSrc.join(',\n')},\n]\n\n` +
    `export const lessonPathById: Record<string, string> = {\n${mapSrc}\n}\n`
  )
}

function emitLesson(m: LessonMeta, importPath: string): string {
  const fields: string[] = [`        id: ${q(m.slug)}`, `        dottedId: ${q(m.dottedId)}`, `        title: ${q(m.title)}`]
  if (m.type) fields.push(`        type: ${q(m.type)}`)
  if (typeof m.order === 'number') fields.push(`        order: ${m.order}`)
  if (typeof m.estimatedMinutes === 'number') fields.push(`        estimatedMinutes: ${m.estimatedMinutes}`)
  if (m.volatility) fields.push(`        volatility: ${q(m.volatility)}`)
  if (m.verifiedAgainstDocsAt) fields.push(`        verifiedAgainstDocsAt: ${q(m.verifiedAgainstDocsAt)}`)
  if (m.prerequisites?.length) fields.push(`        prerequisites: ${JSON.stringify(m.prerequisites)}`)
  if (m.teaches?.length) fields.push(`        teaches: ${JSON.stringify(m.teaches)}`)
  if (m.references?.length) fields.push(`        references: ${JSON.stringify(m.references)}`)
  if (m.docsSources?.length) fields.push(`        docsSources: ${JSON.stringify(m.docsSources)}`)
  if (m.interactive?.length) fields.push(`        interactive: ${JSON.stringify(m.interactive)}`)
  fields.push(`        content: () => import(${q(importPath)})`)
  return `      {\n${fields.join(',\n')},\n      }`
}
